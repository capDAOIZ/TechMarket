// ─── Paginated response factory ───────────────────────────────────────────────
export function buildPaginatedResponse(
  jobs: JobListItem[],
  page: number,
  pageSize: number
): PaginatedJobsResponse {
  const start = (page - 1) * pageSize;
  const items = jobs.slice(start, start + pageSize);
  return {
    items,
    page,
    pageSize,
    total: jobs.length,
    totalPages: Math.ceil(jobs.length / pageSize),
  };
}
